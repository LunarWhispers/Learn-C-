#include "iostream"
using namespace std;

int main() {

//    整型，整数
    7;

//    实型，小数
    182.3;

//    字符型，只能写1个东西，单引号包围
    'h';

//    字符串，啥都能写 ，用双引号包围
    "back world";

    cout <<7<< endl;
    cout << 182 << endl;
    cout << 'h' << endl;
    cout <<" back world "<< endl;
    return 0;

}
